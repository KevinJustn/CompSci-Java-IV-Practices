public class Ken implements Fighter { 
   public void kick(int h) { 
      System.out.println("Ken kicked " + h + " feet high.");
   }
   public void punch(int p) { 
      System.out.println("Ken punched with " + p + " power.");
   }
   public void roll(int d) { 
      System.out.println("Ken rolled " + d + " feet across.");
   }   
} 